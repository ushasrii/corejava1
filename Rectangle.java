class Rectangle{
	public static void main(String[] args){
		double area,perimeter,length,breadth;
		length=20;
		breadth=10;
		area=length*breadth;
		perimeter=2*(length+breadth);
		System.out.println("Area of a Rectangle is: "+area);
		System.out.println("perimeter of a Rectangle is: "+perimeter);
	}
}
